package com.example.ruleteacher.ui.report;

public class losModel {
    private String ID, email, name, classroom, status, docID, gender, age;

    public losModel() {
    }

    public losModel(String ID, String email, String name, String classroom, String status, String docID, String gender, String age) {
        this.ID = ID;
        this.email = email;
        this.name = name;
        this.classroom = classroom;
        this.status = status;
        this.docID = docID;
        this.gender = gender;
        this.age = age;
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getClassroom() {
        return classroom;
    }

    public void setClassroom(String classroom) {
        this.classroom = classroom;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getDocID() {
        return docID;
    }

    public void setDocID(String docID) {
        this.docID = docID;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
